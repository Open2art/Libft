#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	unsigned int	i;

	i = 0;
	if (dstsize == 0)
		return (ft_strlen(src));
	while (src[i] && i < dstsize -1)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize <= ft_strlen(src))
		dst[dstsize - 1] = 0;
	else
		dst[i] = 0;
	return (ft_strlen(src));
}
